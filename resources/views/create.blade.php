        function validatePhone() {
            var phoneInput = document.getElementById("phone");
            var phone = phoneInput.value.trim();
            var phonePattern = /^\d{10}$/;

            if (!phone.match(phonePattern)) {
                alert("Please enter a valid phone number.");
                phoneInput.focus();
                return false;
            } else {
                return true;
            }
        }
